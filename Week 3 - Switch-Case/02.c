#include<stdio.h>
int main()
{
    // increment operators
    // decrement operators\

    int num = 6;
    num += 5;

    printf("%d",num);
    
    num *= 3;
    printf("\n%d",num);

    num -= 3;
    printf("\n%d",num);

    num /= 3;
    printf("\n%d",num);

    int i = 10;

    num += i;
    printf("\n%d",num);

    return 0;
}